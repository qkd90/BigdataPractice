package com.data.data.hmly.action.line;

public class LinetypepriceForm {
	private String 			start;
	private Float			discountPrice;
	private Float			childPrice;
	private Float			rebate;
	private Float			childRebate;
	public String getStart() {
		return start;
	}
	public void setStart(String start) {
		this.start = start;
	}
	public Float getDiscountPrice() {
		return discountPrice;
	}
	public void setDiscountPrice(Float discountPrice) {
		this.discountPrice = discountPrice;
	}
	public Float getChildPrice() {
		return childPrice;
	}
	public void setChildPrice(Float childPrice) {
		this.childPrice = childPrice;
	}
	public Float getRebate() {
		return rebate;
	}
	public void setRebate(Float rebate) {
		this.rebate = rebate;
	}
	public Float getChildRebate() {
		return childRebate;
	}
	public void setChildRebate(Float childRebate) {
		this.childRebate = childRebate;
	}

}
