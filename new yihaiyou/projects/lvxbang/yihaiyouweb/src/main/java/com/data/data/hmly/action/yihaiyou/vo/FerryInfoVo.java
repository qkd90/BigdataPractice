public class FerryInfoVo {

    private String arrivePort;
    private String dailyFlightGid;
    private String departProt;
    private String departTime;
    private Integer freeCount;
    private String number;
    private Float price;
    private String special;
    private String version;
    private String flightLineName;

    public String getArrivePort() {
        return arrivePort;
    }

    public void setArrivePort(String arrivePort) {
        this.arrivePort = arrivePort;
    }

    public String getDailyFlightGid() {
        return dailyFlightGid;
    }

    public void setDailyFlightGid(String dailyFlightGid) {
        this.dailyFlightGid = dailyFlightGid;
    }

    public String getDepartProt() {
        return departProt;
    }

    public void setDepartProt(String departProt) {
        this.departProt = departProt;
    }

    public String getDepartTime() {
        return departTime;
    }

    public void setDepartTime(String departTime) {
        this.departTime = departTime;
    }

    public Integer getFreeCount() {
        return freeCount;
    }

    public void setFreeCount(Integer freeCount) {
        this.freeCount = freeCount;
    }

    public String getNumber() {
        return number;
    }

    public void setNumber(String number) {
        this.number = number;
    }

    public Float getPrice() {
        return price;
    }

    public void setPrice(Float price) {
        this.price = price;
    }

    public String getSpecial() {
        return special;
    }

    public void setSpecial(String special) {
        this.special = special;
    }

    public String getVersion() {
        return version;
    }

    public void setVersion(String version) {
        this.version = version;
    }

    public String getFlightLineName() {
        return flightLineName;
    }

    public void setFlightLineName(String flightLineName) {
        this.flightLineName = flightLineName;
    }
}
